CREATE TABLE ACCOUNT (
  	ID VARCHAR(20) PRIMARY KEY,
  	EMAIL VARCHAR(30),
  	PASSWORD VARCHAR(30),
  	TYPES CHAR,
  	TOKEN VARCHAR(10)

);

CREATE TABLE CIF (
  	ID VARCHAR(20) PRIMARY KEY,
  	EMAIL VARCHAR(30),
  	NAME VARCHAR(20),
  	STATUS CHAR,
  	AGE VARCHAR(5),
  	IDENTIFER VARCHAR(4),
  	IDENTIFERID VARCHAR(20),
  	FRONTID VARCHAR(30),
  	BACKID VARCHAR(30)
);

CREATE TABLE STAFF(
	ID VARCHAR(20) PRIMARY KEY,
	PERMISSION CHAR
);


CREATE TABLE CREDIT(
	ID VARCHAR(20) PRIMARY KEY,
	LIMITD VARCHAR(30) 
);

CREATE TABLE CARDS(
	CID VARCHAR(20) PRIMARY KEY,
	SPENDID VARCHAR(30),
	SAVINGID VARCHAR(30)
);

CREATE TABLE SPEND(
	ID VARCHAR(30),
	BALANCE VARCHAR(30),
	LIMID VARCHAR(30)
);


CREATE TABLE SAVING(
	ID VARCHAR(30),
	BALANCE VARCHAR(30),
	RATE FLOAT
);

CREATE TABLE TRANS(
	FROMID VARCHAR(30),
	TOID VARCHAR(30),
	DESCRIPTION TEXT,
	CHANGE VARCHAR(30),
	CREATEAD DATE
	
);

CREATE TABLE ACTION(
	CREATEBY VARCHAR(30),
	CREATEAT DATE,
	DESCRIPTION TEXT,
	TYPEID VARCHAR(30),
	ACTION VARCHAR(30)
);


